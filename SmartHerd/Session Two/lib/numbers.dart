//methods in dart:core library
void numbers1(){
  //abs()
  var x=75;
  var y=45.6;
  var z=89.247;
  print(x.abs());

  //ceil()
  print(y.ceil());

  //ceilToDouble()
  print(z.ceilToDouble());

  //clamp()
  print(x.clamp(72,78));

  //compareTo()
  print(y.compareTo(55));

  //floor()
  print(z.floor());
}